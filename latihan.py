n1 = int(input ("Nilai ke-1 : "))
n2 = int(input ("Nilai ke-2 : "))
n3 = int(input ("Nilai ke-3 : "))

jumlah = n1 + n2 + n3
kali = n1 * n2 * n3
bagi = n1 / n2 / n3
kurang = n1 - n2 - n3

print("hasil penjumlahan = ", jumlah)
print("hasil perkalian = ", kali)
print("hasil pembagian = ", bagi)
print("hasil pengurangan = ", kurang)